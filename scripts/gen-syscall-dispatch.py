#!/usr/bin/env python3
"""Generate build/dispatch.h from src/syscall/dispatch.tbl."""

from __future__ import annotations

import argparse
import pathlib
import re
import sys

ROOT = pathlib.Path(__file__).resolve().parent.parent
DEFAULT_MANIFEST = ROOT / "src" / "syscall" / "dispatch.tbl"
DEFAULT_ABI = ROOT / "src" / "syscall" / "abi.h"
DEFAULT_OUTPUT = ROOT / "build" / "dispatch.h"
DEFAULT_SYSCALL_SOURCE = ROOT / "src" / "syscall" / "syscall.c"
DISPATCH_TABLE_SIZE = 512

ENTRY_RE = re.compile(r"^(SYS_[A-Za-z0-9_]+)\s+(sc_[A-Za-z0-9_]+)\s+([01])$")
ABI_RE = re.compile(r"^#define\s+(SYS_[A-Za-z0-9_]+)\s+([0-9]+)\s*$")
WRAPPER_RE = re.compile(
    r"^\s*SC_(?:FORWARD|LOCKED|STUB)\((sc_[A-Za-z0-9_]+),", re.MULTILINE
)
HANDLER_RE = re.compile(r"^\s*static\s+int64_t\s+(sc_[A-Za-z0-9_]+)\s*\(", re.MULTILINE)
ALIAS_RE = re.compile(
    r"^\s*#define\s+(sc_[A-Za-z0-9_]+)\s+(sc_[A-Za-z0-9_]+)\s*$", re.MULTILINE
)


def load_sys_numbers(path: pathlib.Path) -> dict[str, int]:
    sys_numbers: dict[str, int] = {}
    for raw_line in path.read_text(encoding="utf-8").splitlines():
        match = ABI_RE.match(raw_line)
        if not match:
            continue
        name, value = match.groups()
        sys_numbers[name] = int(value)
    if not sys_numbers:
        raise ValueError(f"no SYS_* definitions found in {path}")
    return sys_numbers


def parse_manifest(
    path: pathlib.Path, sys_numbers: dict[str, int]
) -> list[tuple[str, object]]:
    items: list[tuple[str, object]] = []
    seen: set[str] = set()

    for lineno, raw_line in enumerate(path.read_text(encoding="utf-8").splitlines(), 1):
        stripped = raw_line.strip()
        if not stripped:
            items.append(("blank", None))
            continue
        if stripped.startswith("##"):
            raise ValueError(f"{path}:{lineno}: unsupported comment prefix '##'")
        if stripped.startswith("#"):
            items.append(("comment", stripped[1:].strip()))
            continue

        match = ENTRY_RE.match(stripped)
        if not match:
            raise ValueError(f"{path}:{lineno}: malformed entry: {stripped}")

        sys_name, handler, extra = match.groups()
        if sys_name not in sys_numbers:
            raise ValueError(f"{path}:{lineno}: unknown syscall constant {sys_name}")
        if sys_name in seen:
            raise ValueError(f"{path}:{lineno}: duplicate entry for {sys_name}")

        seen.add(sys_name)
        items.append(("entry", (sys_name, handler, extra)))

    return items


def load_wrappers(path: pathlib.Path) -> set[str]:
    text = path.read_text(encoding="utf-8")
    wrappers = set(WRAPPER_RE.findall(text))
    wrappers.update(HANDLER_RE.findall(text))
    wrappers.update(alias for alias, _target in ALIAS_RE.findall(text))
    if not wrappers:
        raise ValueError(f"no sc_* wrappers found in {path}")
    return wrappers


def validate_wrappers(
    items: list[tuple[str, object]], wrappers: set[str], path: pathlib.Path
) -> None:
    missing: list[str] = []

    for kind, payload in items:
        if kind != "entry":
            continue
        _, handler, _ = payload
        if handler not in wrappers:
            missing.append(handler)

    if missing:
        missing_list = ", ".join(sorted(missing))
        raise ValueError(f"{path}: missing syscall wrappers: {missing_list}")


def validate_table_size(
    items: list[tuple[str, object]],
    sys_numbers: dict[str, int],
    table_size: int,
    path: pathlib.Path,
) -> int:
    max_nr = -1

    for kind, payload in items:
        if kind != "entry":
            continue
        sys_name, _handler, _extra = payload
        nr = sys_numbers[sys_name]
        if nr >= table_size:
            raise ValueError(
                f"{path}: syscall {sys_name}={nr} exceeds dispatch table size {table_size}"
            )
        if nr > max_nr:
            max_nr = nr

    if max_nr < 0:
        raise ValueError(f"{path}: no syscall entries found")
    return max_nr


def render(items: list[tuple[str, object]], max_syscall_num: int) -> str:
    lines = [
        "/* Linux syscall dispatch table entries",
        " *",
        " * Copyright 2026 elfuse contributors",
        " * SPDX-License-Identifier: Apache-2.0",
        " *",
        " * Generated by scripts/gen-syscall-dispatch.py from",
        " * src/syscall/dispatch.tbl. Do not edit this file directly.",
        " */",
        "",
        "#pragma once",
        "",
        "/* Single source of truth for syscall number -> handler mapping.",
        " * E=0 means only x0-x2 are required by the wrapper.",
        " * E=1 means x3-x5 may also be consumed by the wrapper.",
        " */",
        "",
        f"#define SC_MAX_SYSCALL_NUM {max_syscall_num}",
        "",
        "/* clang-format off */",
        "#define SYSCALL_TABLE_ENTRIES(_)                                   \\",
    ]

    for kind, payload in items:
        if kind == "blank":
            lines.append(
                "                                                                  \\"
            )
            continue
        if kind == "comment":
            text = payload
            lines.append(f"    /* {text} */{' ' * max(1, 58 - len(text))}\\")
            continue

        sys_name, handler, extra = payload
        lines.append(f"    _({sys_name}, {handler}, {extra})          \\")

    if lines[-1].endswith("\\"):
        lines[-1] = lines[-1][:-1].rstrip()
    lines.append("/* clang-format on */")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--manifest", type=pathlib.Path, default=DEFAULT_MANIFEST)
    parser.add_argument("--abi", type=pathlib.Path, default=DEFAULT_ABI)
    parser.add_argument("--output", type=pathlib.Path, default=DEFAULT_OUTPUT)
    parser.add_argument(
        "--syscall-source", type=pathlib.Path, default=DEFAULT_SYSCALL_SOURCE
    )
    parser.add_argument("--check", action="store_true")
    args = parser.parse_args()

    sys_numbers = load_sys_numbers(args.abi)
    items = parse_manifest(args.manifest, sys_numbers)
    max_syscall_num = validate_table_size(
        items, sys_numbers, DISPATCH_TABLE_SIZE, args.manifest
    )
    wrappers = load_wrappers(args.syscall_source)
    validate_wrappers(items, wrappers, args.syscall_source)
    output = render(items, max_syscall_num)

    if args.check:
        if not args.output.exists():
            return 1
        existing = args.output.read_text(encoding="utf-8")
        return 0 if existing == output else 1

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(output, encoding="utf-8")
    return 0


if __name__ == "__main__":
    try:
        raise SystemExit(main())
    except ValueError as exc:
        print(exc, file=sys.stderr)
        raise SystemExit(1)
